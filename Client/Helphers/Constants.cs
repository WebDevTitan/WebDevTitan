        public static bool getCorrectOddsTypeAdmiral(BetburgerInfo info, ref string oddsType, ref string oddsName)
        {
            try
            {
                string outcomeSrc = info.outcome;
                string value = string.Empty;
                GroupCollection groups = Regex.Match(outcomeSrc, "\\((?<value>.*)\\)").Groups;
                if (groups != null && groups["value"] != null)
                    value = groups["value"].Value;

                string outcome = outcomeSrc.Replace(value, string.Empty);

                foreach (string key in Constants.oddTypeHeaderAdmiral.Keys)
                {
                    if (outcome == key)
                    {
                        oddsType = oddTypeHeaderAdmiral[key];

                        if (outcome == "Team1 Win" || outcome == "Team2 Win")
                            oddsType = info.period;

                        if (outcome.Contains("()"))
                        {
                            if (outcome == "AH1()" || outcome == "AH2()")
                            {
                                if (value == "0")
                                    oddsType = "Draw no bet";
                                else
                                {
                                    if (value.Contains(".") || (info.sport == "Tennis"))
                                        oddsType = string.Format(oddsType, string.Empty).Trim();
                                    else
                                    {
                                        if (value.Contains("-"))
                                            oddsType = string.Format(oddsType, string.Format("0:{0}", value.Replace("-", ""))).Trim();
                                        else
                                            oddsType = string.Format(oddsType, string.Format("{0}:0", value.Replace("+", ""))).Trim();
                                    }
                                }
                            }
                            else if (outcome == "TO()" || outcome == "TU()")
                            {
                                if (info.sport == "Tennis")
                                    oddsType = string.Format(oddsType, string.Empty).Trim();
                                else
                                    oddsType = string.Format(oddsType, value.Replace("-", "").Replace("+", "") == "2.5" ? string.Empty : value.Replace("-", "").Replace("+", "").Replace(".", ",")).Trim();
                            }
                            else if (outcome.Contains("TO() for Team1") || outcome.Contains("TU() for Team1"))
                            {
                                if (info.period == "1st half")
                                    oddsType = "Total Goals Team 1 - First Half";
                                else
                                    oddsType = string.Format(oddsType, value.Replace("-", "").Replace("+", "").Replace(".", ",")).Trim();
                            }
                            else if (outcome.Contains("TO() for Team2") || outcome.Contains("TU() for Team2"))
                            {
                                if (info.period == "1st half")
                                    oddsType = "Total Goals Team 2 - First Half";
                                else
                                    oddsType = string.Format(oddsType, value.Replace("-", "").Replace("+", "").Replace(".", ",")).Trim();
                            }
                            else if (outcome.Contains("Both to score"))
                            {
                                if (info.period == "1st half")
                                    oddsType = string.Format(oddsType, "first");
                                else
                                    oddsType = string.Format(oddsType, "second");
                            }
                            else if (outcome.Contains("Score ()"))
                            {
                                oddsName = value;
                            }
                            else if (outcome == "sets ()")
                                oddsName = value;
                        }

                        if (oddsType == "Half-time / Full-time")
                            oddsName = outcome;
                    }

                }

                return true;
            }
            catch (Exception e)
            {
                return false;
            }
        }

