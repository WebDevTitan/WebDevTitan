        public static bool isSameMatch(string mHome, string mAway, string eHome, string eAway, bool bAdvanced = false)
        {
            if (mHome == eHome && mAway == eAway)
                return true;

            if (mHome == eHome && (mAway.Contains(eAway) || eAway.Contains(mAway) || Similarity.Compute(mAway, eAway) <= 5))
                return true;

            if (mAway == eAway && (mHome.Contains(eHome) || eHome.Contains(mHome) || Similarity.Compute(mHome, eHome) <= 5))
                return true;

            if (bAdvanced)
            {
                if ((mHome.Contains(eHome) || eHome.Contains(mHome) || Similarity.Compute(mHome, eHome) <= 5) && (mAway.Contains(eAway) || eAway.Contains(mAway) || Similarity.Compute(mAway, eAway) <= 5))
                    return true;
            }

            return false;
        }

